public class ForLoopMore {
    public static void main(String[] args) {
        // Print Table from 1 to 10
        for(int i=1; i<=10; i++){
            for(int j=1; j<=10; j++){
                System.out.println(i+ " * "+j+" = "+(i*j));
            }
            System.out.println("\n");

        }


    }
}

