import java.util.Locale;
// WAP In JAVA That Find Length of Your Name
public class StringWork {
    public static void main(String[] args) {
        String placeName = "Kathmandu";
        int len = placeName.length();
        String placeUpper = placeName.toUpperCase();
        String placeLower = placeName.toLowerCase();
        System.out.println("The length of "+placeName + " is: "+len);
        System.out.println("IN Uppercase: "+placeUpper);
        System.out.println("In Lowercase: "+placeLower);
    }
}

